number_one=10
number_two=8
#Addition
print(number_one+number_two)        #18
print(type(number_one+number_two))  #<class 'int'>
#Subtraction
print(number_one-number_two)        #2
print(type(number_one-number_two))  #<class 'int'>
#Multuplication
print(number_one*number_two)        #80
print(type(number_one*number_two))  #<class 'int'>
#Division
print(number_one/number_two)        #1.25
print(type(number_one/number_two))  #<class 'float'>
#Integer output Division
print(number_one//number_two)       #1
print(type(number_one//number_two)) #<class 'int'>
#power operator
print(number_one**number_two)       #100000000
print(type(number_one**number_two)) #<class 'int'>
#++ operator is not present
# print(number_one++) Error
number_one=number_one+1
print(number_one)
